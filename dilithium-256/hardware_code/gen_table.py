def fwd_increase(l):
    r1 = 1 << l 
    r2 = 1 << (l + 1)
    i1 = r1
    mask = (r2 - 1)
    ret = [] 
    for i in range(r2 - r1):
        i2 = i1
        i3 = i1 << 1
        i4 = i3 + 1
        if verbose:
            print(i1, i2, i3, i4)

        ret.append([i1, i3, i4])
        
        i1 = (i1 + 1) & mask
    
    return ret

def inv_decrease(l):
    r1 = 1 << l 
    r2 = 1 << (l + 1)
    i1 = r2 - 1
    mask = (r2 - 1)
    ret = []
    for i in range((r2 - r1)//2):
        i2 = i1 - 1
        i3 = i2 >> 1
        i4 = i3
        if verbose:
            print(i1, i2, i3, i4)
        
        ret.append([i1, i2, i4])
        
        i1 = (i1 - 2) & mask
    
    return ret

